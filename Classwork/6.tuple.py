#Tuple

# Empty Tuple
empty_tuple = ()
print(empty_tuple)
# Single-element Tuple (note the trailing comma)
single_tuple = (5,)
print(single_tuple)
# Multi-element Tuple
my_tuple = (1, "apple", 3.5)
print(my_tuple)
# Without parentheses (implicit tuple creation)
implicit_tuple = 1, 2, 3
print(implicit_tuple)